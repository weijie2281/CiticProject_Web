    filters: {
      accNoFormat(str) { //账号四位空格
        if (!str) return str
        let s = ''
        for (let i = 0, len = str.length; i < len; i++) {
          if (i !== 0 && i % 4 === 0) {
            s = s + ' '
          }
          s = s + str[i]
        }
        return s
      }
    },
    methods: {
      //前端搜索功能需要区分是否检索,因为对应的字段的索引不同
      //用两个变量接收currentChangePage函数的参数
      search() {
        if (this.tableDataName == '') {
          this.$message.warning('查询条件不能为空！')
          return
        }
        this.tableDataEnd = []
        //每次手动将数据置空,因为会出现多次点击搜索情况
        this.filterTableDataEnd = []
        this.tableDataBegin.forEach((value, index) => {
          if (value.custAcct) {
            if (value.custAcct.indexOf(this.tableDataName) >= 0) {
              this.filterTableDataEnd.push(value)
            }
          }
        })
        //页面数据改变重新统计数据数量和当前页
        this.currentPage = 1
        this.totalItems = this.filterTableDataEnd.length
        //渲染表格,根据值
        this.currentChangePage(this.filterTableDataEnd)
        //页面初始化数据需要判断是否检索过
        this.flag = true
      },
      handleCurrentChange(val) {
        this.currentPage = val
        //需要判断是否检索
        if (!this.flag) {
          this.currentChangePage(this.tableDataBegin)
        } else {
          this.currentChangePage(this.filterTableDataEnd)
        }
      },
      //组件自带监控当前页码
      currentChangePage(list) {
        let from = (this.currentPage - 1) * this.pageSize
        let to = this.currentPage * this.pageSize
        this.tableDataEnd = []
        for (; from < to; from++) {
          if (list[from]) {
            this.tableDataEnd.push(list[from])
          }
        }
      },
      //重置
      reset() {
        this.tableDataName = ''
        this.totalItems = this.tableDataBegin.length
        this.tableDataEnd = []
        if (this.totalItems > this.pageSize) {
          for (let index = 0; index < this.pageSize; index++) {
            this.tableDataEnd.push(this.tableDataBegin[index])
          }
        } else {
          this.tableDataEnd = this.tableDataBegin
        }
        this.flag = false
      },
      //导出excel表格
      output() {
        console.log(this.multipleSelection)
        var wb = XLSX.utils.table_to_book(document.querySelector('#table'))
        var wbout = XLSX.write(wb, { bookType: 'xlsx', bookSST: true, type: 'array' })
        try {
          FileSaver.saveAs(new Blob([wbout], { type: 'application/octet-stream' }), '账户信息.xlsx')
        } catch (e) {
          if (typeof console !== 'undefined') console.log(e, wbout)
        }
        return wbout
      },
      handleSelectionChange(val) {
        this.multipleSelection = val
      },
      //查看
      accDetail(row) {
        this.rowInfo = row
        this.dialogCheckVisible = true
      },
      transDetail(row) {
        sessionStorage.setItem("detail", JSON.stringify(row.custAcct));
        console.log('detail', row)
        this.$router.push({name: 'tradeDetail', params: {'row': row}})
      },
      moneyFormat(money) { //金额隔三位加,
        if (money && money != null) {
          money = String(money);
          var left = money.split('.')[0], right = money.split('.')[1];
          right = right ? (right.length >= 2 ? '.' + right.substr(0, 2) : '.' + right + '0') : '.00';
          var temp = left.split('').reverse().join('').match(/(\d{1,3})/g);
          return (Number(money) < 0 ? '-' : '') + temp.join(',').split('').reverse().join('') + right;
        } else if (money === 0) { // 注意===在这里的使用，如果传入的money为0,if中会将其判定为boolean类型，故而要另外做===判断
          return '0.00';
        } else {
          return '';
        }
      },
      currencyTypeFormat(row, column) { //币种代码格式化
        let cryType = "_";
        switch (row.ccyCode) {
